class Pin(object):
    def __init__(self, pin, mode):
        self.pin = pin
        self.mode = mode
    def __repr__(self):
        return "Pin(%s, %s)" % (self.pin, self.mode)
    def __eq__(self, other):
        if isinstance(other, Pin):
            # Check if pin is the same, mode is not relevant
            return (self.pin == other.pin)
        else:
            return False
    def __hash__(self):
        return hash(self.pin)


